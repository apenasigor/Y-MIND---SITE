                        <h2 class="head">Diretor 1</h2>
                        <p><div class="type-image" data-id="16484">

                            <div class="image embedded small-size align-right">
                                <img src="//www.jhu.edu/assets/uploads/2014/08/ron_daniels-251x300.jpg" alt="President Ronald J. Daniels">
                            </div>
                        </div></p>
                        <p>Since taking office in March 2009,<strong> Ronald J. Daniels</strong>, Johns Hopkins University's 14th president, has focused his leadership on enhanced interdisciplinary collaboration, individual excellence, and community engagement. These three overarching themes created the backbone of the <em><a href="http://president.jhu.edu/10x2020/">Ten by Twenty</a></em>, a strategic vision for Johns Hopkins through 2020.</p>
                        <p>In his writing, his advocacy, and his personal commitment, President Daniels—a law and economics scholar—has taken a leadership role in a number of areas, including urban engagement, especially within the local Baltimore community; the funding of research in general and young researchers in particular; and fostering innovation and entrepreneurialism.</p>
