            Falando de neuromodulação,<br><br>
            A neuromodulação envolve estimulação cerebral não-invasiva e terapia de ondas cerebrais.
            Ambas podem ser utilizadas de maneira exclusiva ou em associação a outros métodos, para o tratamento de
            quadros clínicos e melhora na performance cognitiva.<br> A neuromodulação não apresenta efeitos colaterais
            significativos; tanto pelo contrário, além de seus efeitos notáveis sobre a inteligência, as técnicas de
            neuromodulação impactam positivamente diversas dimensões existenciais críticas, como sono e motivação,
            além de terem efeito preventivo, aumentando a chamada reserva cognitiva.<br><br>

        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Tratamento</p><br>
            O processo de tratamento envolve duas etapas: avaliação e intervenção.
            Avaliação
            Conforme assinalado, muitos clientes chegam por indicações de seus médicos ou psicólogos, os quais já
            sabem o que eles precisam.<br> Para estes, partimos diretamente para a a apresentação dos procedimentos
            gerais do tratamento e subsequente aplicação dos protocolos clínicos.<br>
            Há, porém, os clientes que chegam sem avaliações diagnósticas atualizadas ou que têm indicação de uma reavaliação. <br>
            Estes deverão passar por uma consulta médica, na qual será realizada a identificação de seu perfil,
            queixas e aspirações, as quais irão nortear o tratamento, seja este baseado em estimulação cerebral
            não-invasiva ou seja este baseado em terapia por ondas cerebrais.<br><br>
            Nossas avaliações são didaticamente separadas em duas modalidades, chamadas clínica e não-clínica,
            conforme descrição a seguir.<br> Ambas envolvem o registro de sinais fisiológicos do cérebro para a criação
            dos mapas de ativação cerebral (EEG quantitativo), que então servem de subsídio científico à definição dos
            protocolos clínicos. <br>Nossa equipe preocupa-se com a comunicação dos resultados dos mapeamentos
            cerebrais para os clientes e seus médicos de origem, os quais costumam ter insights sobre como
            integrá-los ao seu histórico psicológico e, quando aplicado, psiquiátrico, aprofundando os insights produzidos.<br><br>

        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Avaliação Clínica:</p><br>
            Envolve a identificação do quadro psiquiátrico e/ou psicológico do paciente e é realizada por um dos mais
            renomados psiquiatras do país, o Dr. Acioly Lacerda PhD, professor livre-docente do Departamento de
            Psiquiatria da EPM-UNIFESP e diretor clínico da Neuroservices.<br><br> Além da entrevista clínica, os principais
            inventários diagnósticos do mercado estão disponíveis na Neuroservices.<br>
            Nesse primeiro encontro, é realizada a identificação do perfil clínico (ou não clínico) do cliente,
            o que por sua vez é essencial para a definição dos procedimentos clínicos.<br> A sessão inclui também a
            apresentação das técnicas com as quais trabalhamos e todas as explicações necessárias sobre a nossa filosofia clínica.
            <br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Mapeamento cerebral:</p><br>
            Para a avaliação do padrão de ativação cerebral, um equipamento não invasivo de eletroencefalografia quantitativa
        (QEEG) de altíssimo nível é utilizado. O procedimento é simples: eletrodos à seco são dispostos sobre o escalpo e
        os padrões elétricos do cérebro são registrados em três condições: olhos fechados, olhos abertos e durante uma
        tarefa cognitiva. Esses registros são analisados com vista à extração de indicadores do funcionamento neurofisiológico
        do cliente, o que se dá em dois níveis: no primeiro, nossos cientistas procuram indicações de anomalias neurológicas;
        no segundo, o EEG é dividido em faixas frequenciais sabidamente relacionadas a processos psíquicos, cognitivos e
        emocionais e tratamentos estatísticos são realizados para responder acerca de desequilíbrios nestas esferas.

        <br><br><p style="color: #563f80;font-weight: bold;font-size: 22px;">Faixas de ondas:</p><br>
            Delta: as ondas delta estão na faixa de 0.5-3.9Hz e estão relacionadas a um estado de sono, sem sonhos.<br>
            Teta: <br>As ondas teta são encontradas na faixa de 4-7Hz e o aumento de sua ativação está relacionada ao estado
            de sonolência. <br>Na faixa de 7.5hz, teta está relacionada ao processo de codificação da informação, necessário aos
            processos de memória.<br>
            Alfa: as ondas alfa compreendem a faixa de 8-10hz e estão relacionados ao estado de relaxamento-presente.
            Essas ondas aumentam de amplitude durante a meditação.<br><br>
            Beta: as ondas beta na faixa entre 12-23Hz estão relacionadas a processos cognitivos e engajamento em
            atividades que exijam foco externo, como conversa, planejamento e aprendizagem de modo geral.<br>
            Beta-rápida: as ondas beta-rápida, na faixa de 23-38Hz, quando em excesso, estão relacionada a excesso de
            pensamentos e preocupações, levando a vivência de sintomas de ansiedade.

            <br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
        Processos interpretativos dos mapeamentos cerebrais<br>
        </p>
            A tradução dos mapeamentos cerebrais em indicativos eficientes de tratamento é levada muito à sério na
            Clínica Neuroservices.<br><br> Nosso procedimento geral parte de dois princípios: identificação das principais
            assinaturas neurais e conexão delas ao perfil comportamental do cliente.<br>

        <br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Assinaturas neurais</p><br>
            As assinaturas neurais compreendem os principais padrões de ativação que podem ser encontrados a partir
            dos resultados do mapeamento cerebral por QEEG.<br><br> De modo geral, alguns padrões são mais prevalentes,
            fazendo com que certas manifestações sintomáticas aflorem, ou ainda que haja perdas específicas de
            desempenho cognitivo em relação ao pleno potencial do cliente.<br><br> Através de nossa experiência clínica e
            liderança científica, identificamos estes padrões e direcionamentos os tratamentos com eficiência
            comprovada internacionalmente.<br>
            Um fato digno de nota é que, por força das tendências autorregulatórias do cérebro, é comum que ajustes em
            determinados padrões frequenciais impactem positivamente outros tantos, produzindo efeitos sistêmicos.<br>

        <br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Perfis Comportamentais</p><br>
            Perfis comportamentais são estruturas comportamentais de caráter abrangente que permitem maior
            assertividade no delineamento de tratamentos baseados em neuromodulação, sobretudo quando associados a
            avaliações psiquiátricas bem fundamentadas, diagnósticos por imagem e QEEG. Eles representam a contrapartida
            exterior (fenótipo) àquilo que ocorre dentro do cérebro (endofenótipo), ditando padrões de adoecimento e
            desvios na capacidade de realização plena do potencial cognitivo.<br><br>
            A ideia subjacente ao seu uso é de que queixas clínicas podem ser compreendidas em sua especificidade a
            partir de desequilíbrios entre estes perfis, que deste modo operam como um registro paralelo ao diagnóstico
            psiquiátrico.<br><br> Assim, por exemplo, pode-se considerar que existem diversos perfis comportamentais sob
            o guarda-chuva da depressão ou do déficit de atenção, o qual estão relacionados a assinaturas neurológicas
            específicas (endofenótipos) e devem direcionar o cliente para protocolos diferentes de neuromodulação.<br>
            Alguns destes perfis variam sobre o eixo ativação-desativação, outros variam em torno
            do eixo da valência, isto é, da atribuição de sentido afetivo às experiências, ao passo que também
            perfis associados ao predomínio de modalidades de processamento cognitivo, chamadas de biocomputações simbólicas. <br>
            Todos estes apresentam correspondentes endofenotípicos, o que justamente nos permite utilizá-los no racional
            dos tratamentos. <br>Na sessão diagnóstica, são feitas explorações visando o mapeamento do perfil
            comportamental do cliente, o que ajuda a refinar o direcionamento.<br><br>
            A título de exemplo, citamos três perfis que podem ser utilizados no direcionamento da prática clínica
            baseada em neuromodulação; na prática, há vários outros:<br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
            Perfil 1: Baixa ativação e iniciativa</p><br>
            O indivíduo caracterizado por baixa ativação tende a se sentir sem energia durante grande parte do dia,
            relatando cansaço físico, mental ou ambos. Porém, a característica comportamental da iniciativa faz com que
            ele procure novas atividades e sensações a todo momento, muitas vezes aparentando aos outros como alguém
            com energia ou até agitado. Assim, há uma dissonância entre o comportamento observado externamente,
            muitas vezes voltado para a ação, e o estado interno percebido pelo próprio sujeito. Nesses casos,
            é comum a verbalização da sensação de cansaço ou sonolência constante ao longo do dia, e de que se a pessoa
            parar de fazer as atividades da vida diária, especialmente as físicas, ela simplesmente dorme.  É comum que
            o sujeito do perfil tipo 1 apresente queixas cognitivas como dificuldades de sustentar a atenção por períodos
            de tempo em atividades cognitivas e ou monótonas, queixas de esquecimento ou perda de objetos por não saber onde
            os colocou, dificuldade de gerenciar compromissos, dentre outras.<br><br>

        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
            Hipóteses de correlatos fisiológicos: </p><br>

            excesso de ondas lentas, especialmente na região frontal, mas que podem
            se espalhar por todo o córtex; alfa 1 (faixa 8-10Hz) com maiores amplitudes, algumas vezes maiores que as
            amplitude na faixa de alfa 2 (faixa 10-12); relação entre as ondas teta e beta alta; baixa ativação de ondas
            beta 1, especialmente nas região frontal até a linha central; pode apresentar excesso de ondas beta-rápida em diversas áreas.<br><br>


        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
            Treinamento recomendado para sujeitos do perfil 1: </p>
        <br>Os protocolos para esse perfil tendem a buscar o aumento
            da energia e ativação biológica, correspondendo ao aumento na sensação física e mental de disposição e
            melhora nos processos cognitivos básicos como manutenção da atenção e aumento na capacidade de
            memória operacional, afetando processos mais complexos como raciocínio, planejamento e tomada de decisão. <br><br>
            Assim, uma certa combinação de protocolos pode ser utilizada para alcançar-se esses objetivos, como:<br><br>
            - Treinamento para aumento da perfusão pré-frontal com HEG neurofeedback;<br>
            - Treinamento de redução na amplitude das ondas lentas ou do EEG total, de acordo com o achado fisiológico mais evidente;<br>
            - Treinamento para aumento na amplitude das ondas beta 1, incluindo o ritmo sensório motor com EEG neurofeedback;<br>
            - Treinamento de redução na amplitude das ondas beta-rápido com EEG neurofeedback (quando aplicável);<br>
            - Estimulação transcraniana por corrente contínua na região frontal do cérebro;<br>
            - Remediação ou otimização cognitiva (quando as queixas cognitivas forem evidentes e foco do tratamento).<br><br>

        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Perfil 2: Baixa ativação e evitação
        </p><br>
            Assim como o perfil acima, o indivíduo com tônus de baixa ativação tende a se sentir sem energia durante
            grande parte do dia, relatando cansaço físico, mental ou ambos. Porém, a sua característica comportamental
            marcante é o medo (frequentemente expresso enquanto evitação) de situações que, de forma consciente ou
            inconsciente, avalia como potencialmente estressantes; trata-se, enfim, do sujeito paralisado por
            sentimentos persecutórios.<br> Para esse indivíduo o mundo pode parecer mais agressivo e assustador que
            para as outras pessoas e sua sensação de falta de energia física e emocional faz com que ele não consiga
            enfrentar os desafios presentes no seu dia a dia. Nos períodos em que os sintomas estão mais
            intensos, o indivíduo com este perfil pode apresentar pensamentos excessivos de preocupação acerca de
            sua integridade física, psicológica e moral, respaldada em fatos da realidade de maneira exageradamente
            negativa ou em pensamentos catastróficos sem fatores reais que os subsidiem; em suma, apresenta traços
            paranoides sem um quadro esquizofreniforme totalmente manifesto (classicamente, este quadro denomina-se
            parafrenia, mas tal termo praticamente caiu em desuso). Tende a apresentar queixas de desmotivação
            basedas em tensões conflitivas paralisantes, que podem afetar seu senso e capacidade de realização de
            maneira global.<br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Endofenótipo:</p> <br>excesso de ondas lentas, especialmente na região frontal, mas que podem se espalhar por
            todo o córtex; relação entre as ondas teta e beta alta; baixas amplitudes de ondas alfa por todo o
            cérebro; baixas amplitudes de beta 1 por todo o cérebro; tende a apresentar inversões, especialmente na
            região frontal; pode apresentar excesso de ondas beta-rápida; distúrbios dopaminérgicos frontolímbicos,
            tal como caracterizados em uma miríade de artigos científicos de alto impacto.<br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">Tratamento recomendado para sujeitos do perfil 2:</p><br> Os protocolos para esse perfil tendem a buscar o aumento
            da energia cognitiva para a inibição límbica, com vias ao fortalecimento simultâneo de processos cognitivos
            e da estabilização afetivo-emocional. Dentre a combinação de protocolos, tende-se a priorizar:<br><br>

        <p style="color: #563f80;">- Treinamento para aumento da perfusão pré-frontal com HEG neurofeedback;<br>
            <br>- Treinamento de redução na amplitude das ondas lentas e aumento na amplitude das ondas beta 1, principalmente
            no lobo frontal indo até a região central, incluindo o ritmo sensório motor, com EEG neurofeedback;<br>
            <br>- Treinamento para aumento direto ou indireto das ondas alfa com EEG neurofeedback;<br>
            <br>- Treinamento para inversões, reforçando a prevalência de beta no hemisfério esquerdo e alfa no hemisfério
            direito com EEG neurofeedback (quando aplicável);<br>
            <br>- Estimulação magnética transcraniana na região frontal do cérebro;<br>
            <br>- Remediação ou otimização cognitiva (em uma segunda fase, quando os sintomas afetivo-emocionais se
            mostrarem estabilizados).</p><br>
            <br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
        Perfil 3: Alta ativação e iniciativa, com processos hipomaníacos e excessiva ansiedade
        </p>
        <br>
            O indivíduo com tônus de alta ativação tende a se engajar positivamente em múltiplas tarefas,
            porém sofre com ondas de ansiedade, dificuldades para a execução efetiva de comportamentos inibitórios
            (o que acarreta em excessivo número de ações impulsivas), dificuldade para se desligar de rotas pragmáticas
            (o que acarreta constante tensão corporal e múltiplos sintomas somatoformes) e problemas de sono. <br><br>
            Trata-se do típico indivíduo vulnerável a acidentes vasculares prematuros ou a eventos cardíacos fulminantes. <br>
            Dentre as queixas cognitivas, é comum o relato de dificuldades na definição de prioridades e no atingimento
            de estados satisfatórios, em qualquer domínio existencial. <br>Em geral, esse perfil gera a estruturação da
            vida social em torno de laços pragmáticos, desde o rechaço espontâneo de pessoas de perfis menos
            energizados, o que nem sempre é percebido pelo indivíduo por este ângulo.<br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
            Hipóteses de correlatos fisiológicos: </p>

        <br>EEG “acelerado”: pico de alfa pode estar em frequências mais altas,
            assim como o pico de beta; cérebro muito coerente, especialmente nas regiões fronto-parietais;
            altas amplitudes em quase todas as frequências, deixando o histograma mais achatado; pode apresentar excesso
            de ondas beta-rápida nas regiões fronto-temporais; pode (ou não) apresentar baixa porcentagem do
            ritmo sensório-motor,<br><br>
        <p style="color: #563f80;font-weight: bold;font-size: 22px;">
            Tratamento recomendado para sujeitos do perfil 3:
        </p>
        <br>Os protocolos para esse perfil tendem a buscar maior conexão
            interior, através de injeções de afetos em modelos mentais originalmente frios e pragmáticos; em paralelo,
            busca-se economia energética no cérebro e aumento da sensação de relaxamento.<br> Essas alterações tendem
            trazer aumento espontâneo do controle inibitório, facilitando processos mais complexos como definição de
            prioridades e redução nos níveis de estresse. <br>Dentre a combinação de protocolos, tende-se a priorizar:<br><br>
        <p style="color: #563f80;">
            - Treinamento com biofeedback cardiovascular, aumentando o equilíbrio entre os sistemas autonômicos de ativação fisiológica;<br>
            <br>- Treinamento de redução na amplitude das beta rápida ou do EEG total, conforme
            e aumento na amplitude das ondas beta 1, principalmente no lobo frontal indo até a região central, incluindo o ritmo sensório
            motor, com EEG neurofeedback;<br>
            <br>- Treinamento para aumento direto ou indireto das ondas alfa com EEG neurofeedback;<br>
            <br>- Treinamento para inversões, reforçando a prevalência de beta no hemisfério esquerdo e
            alfa no hemisfério direito com EEG neurofeedback (quando aplicável);<br>
            <br>- Estimulação transcraniana por corrente contínua na região frontal do cérebro;<br>
            <br>- Remediação ou otimização cognitiva (em uma segunda fase, quando os sintomas afetivo-emocionais se mostrarem estabilizados).


